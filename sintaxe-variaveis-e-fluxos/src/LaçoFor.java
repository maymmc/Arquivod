
public class LaçoFor {
	public static void main(String[] args) {
		
		//i =contador
		//for(estrutura de repetição/laço) inicialização;     até onde vai;     passo/ de quanto e quanto ele vai;
		// ex: 3 em 3, 2 em 2, 1 em 1
		for (int contador = 1; contador <= 10; contador++)
		//for (int contador = 10; contador <= 10; contador--)
		{
			System.out.println(contador);
		}

		int contador = 1;
		while (contador <= 10) {
			System.out.println(contador);
			contador++;
		}
	}

}
